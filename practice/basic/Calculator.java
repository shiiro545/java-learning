public class Calculator {

    public static void main(String[] args) {

        int x = 15;
        int y = 7;

        System.out.println("足し算 = " + (x + y));
        System.out.println("引き算 = " + (x - y));
        System.out.println("掛け算 = " + (x * y));
        System.out.println("割り算 = " + (x / y));
    }
}